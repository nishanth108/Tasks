public class WhiteBoard{
		public static void boardUse(String use){
		System.out.println(use);
	} 
	public static void boardProjecting(String projecting){
		System.out.println(projecting);
	} 
	public static void boardAllign(String align){
		System.out.println(align);
	}
	public static void boardTeach(String teacher,int hours){
		System.out.println(teacher+hours);
	} 
	public static void boardDetails(float height,float width){
		System.out.println(height+width);
	} 	
	public static void boardColor(String color,String penColor,boolean writing){
		System.out.println(color+penColor+" "+writing);
	} 
	public static void boardInstitute(String Name,int distance,char section){
		System.out.println(Name+distance+section);
	} 		
}